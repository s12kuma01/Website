// Generates src/styles/generated/motion.css — the 6 M3 Expressive spring
// tokens compiled to CSS linear() easings (80 curvature-weighted samples)
// plus their settle durations. 12 custom properties total.

import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { EXPRESSIVE_SPRINGS, springLinearEasing } from './lib/spring.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const OUT = join(here, '..', 'src', 'styles', 'generated', 'motion.css');

const hash = createHash('sha256')
  .update(readFileSync(fileURLToPath(import.meta.url)))
  .update(readFileSync(join(here, 'lib', 'spring.mjs')))
  .digest('hex')
  .slice(0, 16);
const header = `/* gen:${hash} — generated by scripts/gen-motion-tokens.mjs; do not edit */`;

if (existsSync(OUT) && readFileSync(OUT, 'utf8').startsWith(header)) {
  console.log('[gen-motion-tokens] up to date, skipped');
  process.exit(0);
}

const decls = [];
for (const [name, { zeta, stiffness }] of Object.entries(EXPRESSIVE_SPRINGS)) {
  const { easing, durationMs } = springLinearEasing(zeta, stiffness, 80);
  decls.push(`  --md-sys-motion-spring-${name}-easing: ${easing};`);
  decls.push(`  --md-sys-motion-spring-${name}-duration: ${durationMs}ms;`);
}

const css = [header, ':root {', ...decls, '}'].join('\n');
mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, css);
console.log(`[gen-motion-tokens] wrote ${decls.length} custom properties -> ${OUT}`);
