// Generates src/styles/generated/palettes.css — 36 precomputed M3E dynamic
// color schemes (hues 0..350 in 10° steps), light + dark, 49 color roles
// each. Runs before astro dev/build (explicit chain in package.json;
// pnpm does not run pre* lifecycle hooks).

import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { schemeCssDecls } from './lib/scheme-css.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const OUT = join(here, '..', 'src', 'styles', 'generated', 'palettes.css');
export const THEME_COUNT = 36;

// Input hash: skip regeneration when nothing that shapes the output changed.
const mcuVersion = JSON.parse(
  readFileSync(join(here, '..', 'node_modules', '@material', 'material-color-utilities', 'package.json'), 'utf8'),
).version;
const hash = createHash('sha256')
  .update(readFileSync(fileURLToPath(import.meta.url)))
  .update(readFileSync(join(here, 'lib', 'scheme-css.mjs')))
  .update(`mcu@${mcuVersion};N=${THEME_COUNT}`)
  .digest('hex')
  .slice(0, 16);
const header = `/* gen:${hash} — generated by scripts/gen-color-tokens.mjs; do not edit */`;

if (existsSync(OUT) && readFileSync(OUT, 'utf8').startsWith(header)) {
  console.log('[gen-color-tokens] up to date, skipped');
  process.exit(0);
}

// Each theme is a single block of light-dark() pairs; light vs dark is
// selected by color-scheme (base.css maps data-mode -> color-scheme), so
// there is no dark media query and a manual mode toggle re-resolves live.
const blocks = [];
for (let i = 0; i < THEME_COUNT; i++) {
  const hue = i * (360 / THEME_COUNT);
  blocks.push(`[data-theme="${i}"]{${schemeCssDecls(hue)}}`);
}

// The no-JS fallback (theme 0) is wrapped in :where() — zero specificity —
// so it can never beat a [data-theme="i"] block OR the runtime-injected
// #theme-live stylesheet, regardless of stylesheet order (Vite injects
// styles late in dev; the boot <style> precedes the <link> in prod).
const css = [header, `:where(:root){${schemeCssDecls(0)}}`, ...blocks].join('\n');

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, css);
console.log(`[gen-color-tokens] wrote ${THEME_COUNT} light-dark schemes -> ${OUT}`);
